const refs = {
  moviePic: () => document.querySelector('.imgFilm'),
};

function replaceEmptyPic() {
  const picSource = refs.moviePic().getAttribute('src');
  if (picSource === './images/film1.jpg') {
    refs.moviePic().setAttribute('src', '../images/movie_placeholder.svg');
  }
}
replaceEmptyPic();
